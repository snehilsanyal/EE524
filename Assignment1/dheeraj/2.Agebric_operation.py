print("enter two number a and b")
a=int(input())
b=int(input())
# summation
c=a+b
print(a,"+",b,"=",c)
#substraction
d=a-b
print(a,"-",b,"=",d)
#multiplication
e=a*b
print(a,"*",b,"=",e)
#division

if b!=0:
    f=a/b
    print(a, "/", b, "=", f)

#remainder
if b!=0:
    g = a % b
    print(a, "%", b, "=", g)
